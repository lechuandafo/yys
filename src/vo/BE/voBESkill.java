package vo.BE;

public class voBESkill {
	private String skillNo;
	private String skillName;
	private String skillDescription;
	private String baseCoefficient;
	public String getSkillNo() {
		return skillNo;
	}
	public void setSkillNo(String skillNo) {
		this.skillNo = skillNo;
	}
	public String getSkillName() {
		return skillName;
	}
	public void setSkillName(String skillName) {
		this.skillName = skillName;
	}
	public String getSkillDescription() {
		return skillDescription;
	}
	public void setSkillDescription(String skillDescription) {
		this.skillDescription = skillDescription;
	}
	public String getBaseCoefficient() {
		return baseCoefficient;
	}
	public void setBaseCoefficient(String baseCoefficient) {
		this.baseCoefficient = baseCoefficient;
	}
}
