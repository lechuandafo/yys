package vo.OD;

public class voODProductSell {
	private String serviceNo;
	private String commodityNo;
	private int price;
	private int ccountSum;
	private int cSum;
	public String getServiceNo() {
		return serviceNo;
	}
	public void setServiceNo(String serviceNo) {
		this.serviceNo = serviceNo;
	}
	public String getCommodityNo() {
		return commodityNo;
	}
	public void setCommodityNo(String commodityNo) {
		this.commodityNo = commodityNo;
	}
	public int getPrice() {
		return price;
	}
	public void setPrice(int price) {
		this.price = price;
	}
	public int getCcountSum() {
		return ccountSum;
	}
	public void setCcountSum(int ccountSum) {
		this.ccountSum = ccountSum;
	}
	public int getcSum() {
		return cSum;
	}
	public void setcSum(int cSum) {
		this.cSum = cSum;
	}
	
}
